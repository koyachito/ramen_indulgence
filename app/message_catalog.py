def _load_date_reasons() -> dict[str, str]:
    reasons = {}
    for line in DATE_REASONS_PATH.read_text(encoding="utf-8").splitlines():
        if line.strip():
            key, text = line.split(",", 1)
            reasons[key] = text
    return reasons


